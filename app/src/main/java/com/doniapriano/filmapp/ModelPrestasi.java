package com.doniapriano.filmapp;

public class ModelPrestasi {

    private String namaLomba, juara, tingkat, tanggal;

    public String getNamaLomba() {
        return namaLomba;
    }

    public void setNamaLomba(String namaLomba) {
        this.namaLomba = namaLomba;
    }

    public String getJuara() {
        return juara;
    }

    public void setJuara(String juara) {
        this.juara = juara;
    }

    public String getTingkat() {
        return tingkat;
    }

    public void setTingkat(String tingkat) {
        this.tingkat = tingkat;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }
}
